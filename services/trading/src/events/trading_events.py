@register_event
@dataclass(kw_only=True)
class PositionOpened(TradingEvent):
    """A new position was opened.

    Emitted when first shares of a symbol are acquired.
    """

    EVENT_TYPE: ClassVar[str] = "position.opened"

    symbol: str
    side: Literal["long", "short"]
    qty: Decimal
    entry_price: Decimal
    order_id: UUID  # The fill that opened this position


@register_event
@dataclass(kw_only=True)
class PositionIncreased(TradingEvent):
    """An existing position was increased.

    More shares added to an existing position.
    """

    EVENT_TYPE: ClassVar[str] = "position.increased"

    symbol: str
    qty_added: Decimal
    price: Decimal
    new_total_qty: Decimal
    new_avg_cost: Decimal
    order_id: UUID


@register_event
@dataclass(kw_only=True)
class PositionReduced(TradingEvent):
    """An existing position was reduced.

    Some shares sold/covered but position remains open.
    """

    EVENT_TYPE: ClassVar[str] = "position.reduced"

    symbol: str
    qty_removed: Decimal
    exit_price: Decimal
    remaining_qty: Decimal
    realized_pnl: Decimal
    order_id: UUID


@register_event
@dataclass(kw_only=True)
class PositionClosed(TradingEvent):
    """A position was fully closed.

    All shares sold/covered.
    """

    EVENT_TYPE: ClassVar[str] = "position.closed"

    symbol: str
    exit_price: Decimal
    realized_pnl: Decimal
    order_id: UUID


# =============================================================================
# Session Events
