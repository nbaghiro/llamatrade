@register_event
@dataclass(kw_only=True)
class SessionStarted(TradingEvent):
    """Trading session was started."""

    EVENT_TYPE: ClassVar[str] = "session.started"

    strategy_id: UUID
    strategy_name: str
    mode: Literal["live", "paper"]
    symbols: list[str] = field(default_factory=list)
    starting_equity: Decimal = Decimal("0")


@register_event
@dataclass(kw_only=True)
class SessionStopped(TradingEvent):
    """Trading session was stopped."""

    EVENT_TYPE: ClassVar[str] = "session.stopped"

    reason: str  # "user_requested", "error", "end_of_day", etc.
    final_equity: Decimal | None = None
    total_pnl: Decimal | None = None


@register_event
@dataclass(kw_only=True)
class SessionPaused(TradingEvent):
    """Trading session was paused."""

    EVENT_TYPE: ClassVar[str] = "session.paused"

    reason: str


@register_event
@dataclass(kw_only=True)
class SessionResumed(TradingEvent):
    """Trading session was resumed."""

    EVENT_TYPE: ClassVar[str] = "session.resumed"


# =============================================================================
# Circuit Breaker Events
# =============================================================================


@register_event
@dataclass(kw_only=True)
class CircuitBreakerTriggered(TradingEvent):
    """Circuit breaker was triggered, trading paused."""

    EVENT_TYPE: ClassVar[str] = "circuit_breaker.triggered"

    reason: str  # "consecutive_losses", "daily_loss_limit", etc.
    details: dict = field(default_factory=dict)


@register_event
@dataclass(kw_only=True)
class CircuitBreakerReset(TradingEvent):
    """Circuit breaker was reset, trading can resume."""

    EVENT_TYPE: ClassVar[str] = "circuit_breaker.reset"

    was_forced: bool = False  # True if reset before cooldown elapsed


# =============================================================================
# Reconciliation Events
# =============================================================================


@register_event
@dataclass(kw_only=True)
class ReconciliationPerformed(TradingEvent):
    """Position reconciliation was performed with broker.

    Emitted when we sync our state with the broker's actual state.
    """

    EVENT_TYPE: ClassVar[str] = "reconciliation.performed"

    positions_added: list[str] = field(default_factory=list)
    positions_removed: list[str] = field(default_factory=list)
    positions_adjusted: list[str] = field(default_factory=list)
    orders_synced: int = 0
    discrepancies_found: int = 0


@register_event
@dataclass(kw_only=True)
class ExternalTradeDetected(TradingEvent):
    """A trade was detected that wasn't initiated by our system.

    E.g., manual trade via broker dashboard.
    """

    EVENT_TYPE: ClassVar[str] = "reconciliation.external_trade"

    symbol: str
    side: Literal["buy", "sell"]
    qty: Decimal
    price: Decimal
    broker_order_id: str
